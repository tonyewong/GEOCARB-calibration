pick_chains <- function(n) {
    use_chains <- rep(TRUE, n)
    {for (m in 1:n) {use_chains[m] <- as.logical(readline(prompt=paste("Use chain",m,"? Enter TRUE or FALSE. ")))}};
    return(use_chains)
}

